class Arith{
public static void main(String[] args) {
    int a = 15;
    short s = 25;
    double d = 42.35;
    float f = 6.21f;
    double sum = d+a;
    float sub = s-f;
    int multi = s*a;
    float div = (float)d/f;
    System.out.println(sum);
    System.out.println(sub);
    System.out.println(multi);
    System.out.println(div);

}
}